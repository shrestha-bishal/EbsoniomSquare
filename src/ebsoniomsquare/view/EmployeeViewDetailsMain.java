    private void viewEmployeeDetailsTblMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_viewEmployeeDetailsTblMouseClicked
        //Display the selected fields in the Text Box
        int i = viewEmployeeDetailsTbl.getSelectedRow();
        TableModel model= viewEmployeeDetailsTbl.getModel();
        
        branchNameLbl.setText(model.getValueAt(i,1).toString());
        departmentFld.setText(model.getValueAt(i,2).toString());
        designationFld.setText(model.getValueAt(i,3).toString());
        String salutation = model.getValueAt(i,4).toString();
        if(salutation.equals("Mr")){
            titleMrOp.setSelected(true);
            titleMrsOp.setSelected(false);
            titleMsOp.setSelected(false);
            titleOtherOp.setSelected(false);
        }if(salutation.equals("Mrs")){
            titleMrOp.setSelected(false);
            titleMrsOp.setSelected(true);
            titleMsOp.setSelected(false);
            titleOtherOp.setSelected(false);
        }if(salutation.equals("Ms")){
            titleMrOp.setSelected(false);
            titleMrsOp.setSelected(false);
            titleMsOp.setSelected(true);
            titleOtherOp.setSelected(false);
        }if(salutation.equals("Other")){
            titleMrOp.setSelected(false);
            titleMrsOp.setSelected(false);
            titleMsOp.setSelected(false);
            titleOtherOp.setSelected(true);
        }
        nameFld.setText(model.getValueAt(i,5).toString());
        String gender = model.getValueAt(i,6).toString();
        if(gender.equals("Male")){
            genderMaleOp.setSelected(true);
            genderFemaleOp.setSelected(false);
            genderOtherOp.setSelected(false);
        }if(gender.equals("Female")){
            genderMaleOp.setSelected(false);
            genderFemaleOp.setSelected(true);
            genderOtherOp.setSelected(false);
        }if(gender.equals("other")){
            genderMaleOp.setSelected(false);
            genderFemaleOp.setSelected(false);
            genderOtherOp.setSelected(true);
        }
        String maritialStatus = model.getValueAt(i,7).toString();
        if(maritialStatus.equals("Single")){
            maritialSingleOp.setSelected(true);
            maritialMarriedOp.setSelected(false);
        }if(maritialStatus.equals("Married")){
            maritialSingleOp.setSelected(false);
            maritialMarriedOp.setSelected(true);
        } 
        nationalityFld.setText(model.getValueAt(i,8).toString());
        try{
           Date date= new SimpleDateFormat("yyyy-MM-dd").parse((String)model.getValueAt(i,9));
           dobFld.setDate(date);
        }catch(Exception e){
           JOptionPane.showMessageDialog(null, e);
            }
        educationFld.setText(model.getValueAt(i,10).toString());
        fatherNameFld.setText(model.getValueAt(i,11).toString());
        motherNameLbl.setText(model.getValueAt(i,12).toString());
        permanentAddressFld.setText(model.getValueAt(i,13).toString());
        temporaryAddressFld.setText(model.getValueAt(i,14).toString());
        mobileNumberLbl.setText(model.getValueAt(i,15).toString());
        emailIdFld.setText(model.getValueAt(i,16).toString());
        usernameFld.setText(model.getValueAt(i,17).toString());
        mobileNumberLbl.setText(model.getValueAt(i,18).toString());   
    }//GEN-LAST:event_viewEmployeeDetailsTblMouseClicked

