            row[8] = list.get(i).getEducation();
            row[9] = list.get(i).getFather(); 
            row[10] = list.get(i).getMother();
            row[11] = list.get(i).getPermanentAddress();
            row[12] = list.get(i).getTemporaryAddress();
            row[13] = list.get(i).getContact();
            row[14] = list.get(i).getEmail(); 
