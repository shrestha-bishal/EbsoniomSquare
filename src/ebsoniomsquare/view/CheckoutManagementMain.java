/*Delete from table*/
    private void deleteFromTable(int row){
        DefaultTableModel model=(DefaultTableModel)checkOutTable.getModel();
        model.removeRow(row);
        setTotalAmount(model);
    }
    /*Set Total Amount*/
   private void setTotalAmount(DefaultTableModel model){
       int row = model.getRowCount();
       System.out.println(row);
        if(row==0){
            totalField.setText("0");
            discountField.setText("0");
            grandTotalField.setText("0");
        }else{
            double sum = 0;
       for(int i = 0;i < row; i++){
             double total=(double) checkOutTable.getModel().getValueAt(i, 3);//price;
               System.out.println(total+"  "+ row);
             sum=sum+total;
        }
        totalField.setText(String.valueOf(sum));
            discountField.setText(String.valueOf(0.1*sum));
            grandTotalField.setText(String.valueOf(sum-0.1*sum));
        }
    }
    /*get Data in the table;*/
    private void getDataInTable(CheckoutModel checkOut){
        ProductServices productService = new ProductServicesImpl();
        ProductModel productModel = productService.getById(checkOut.getPid());
        DefaultTableModel model=(DefaultTableModel)checkOutTable.getModel();
        //model.setRowCount(0);
        	model.addRow(new Object[] {productModel.getPid(),productModel.getProductName()
                        ,checkOut.getQuantity(),
                        (Double.parseDouble(checkOut.getQuantity()))*(Double.parseDouble(productModel.getPrice())),
                        });
	        setTotalAmount(model); 
    }
