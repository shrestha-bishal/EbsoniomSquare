 //Task Performed when CheckOut Button is Clicked
    private void checkOutbuttonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_checkOutbuttonActionPerformed
        CheckoutModel checkoutModel = new CheckoutModel();
        checkoutModel.setBillingNo(billNo.getText());
        Date date = new Date();
        String currentDate = "";
        currentDate = String.valueOf(date);
        checkoutModel.setCurrentDate(currentDate);
        checkoutModel.setCustomerID(customerID.getText());
        checkoutModel.setDiscount(discountField.getText());
        checkoutModel.setGrandTot(grandTotalField.getText());
        checkoutModel.setTotalAmount(totalField.getText());
        checkoutModel.setReceivedAmount(receivedField.getText());
        checkoutModel.setReturnAmount(returnField.getText());
        CheckoutServices checkoutServices = new CheckoutServicesImpl();
        if(!(checkoutModel.getBillingNo()==""||checkoutModel.getGrandTot()==""||checkoutModel.getReceivedAmount()==""||checkoutModel.getReturnAmount()==""||checkoutModel.getBillingNo()=="")){
            MessageParamClass message = new MessageParamClass("Sure to Checkout?", "EbsoniomSquare Message"); 
            int returnOptionValue = message.showConfirmDialog();
            if(returnOptionValue==0){//0 is yes
                DefaultTableModel getModel = (DefaultTableModel)checkOutTable.getModel();
                int rowofTable = getModel.getRowCount();
                for(int countRow =0; countRow<rowofTable; countRow++){
                    String pid1 = (String)getModel.getValueAt(countRow, 0);
                    String quantity1 = (String)getModel.getValueAt(countRow, 2);
                    checkoutModel.setPid(pid1);
                    checkoutModel.setQuantity(quantity1);
                    if(checkoutServices.checkoutAll(checkoutModel)){
                        JOptionPane.showMessageDialog(null, "CheckedOut SuccessFully");
                        getModel.setRowCount(0);
                        generateReceipt(); 
                    }
                }
            }
        }else{
            JOptionPane.showMessageDialog(null, "Some Field is still null!!");
        }
        
    }//GEN-LAST:event_checkOutbuttonActionPerformed
