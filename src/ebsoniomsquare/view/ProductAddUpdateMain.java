/*
    Function to show table data on form load
*/
    public void showProductTableData(){
        System.out.println("Reference working");
        ProductServices productServices=new ProductServicesImpl();
        List<ProductModel> productList=productServices.showAllProduct();
        DefaultTableModel model=(DefaultTableModel)productTable.getModel();
        model.setRowCount(0);
	for(ProductModel p: productList) {
        	model.addRow(new Object[] {p.getPid(),p.getProductName(),p.getQuantity(),p.getPrice()});
	}
    }
