//proceed to Update Product on Update Button Clicked
    private void updateButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updateButtonActionPerformed
                    if(productTable.getSelectedRow()<0) {
                        JOptionPane.showMessageDialog(null, "Please select any product");
			return;
                    }
		int row=productTable.getSelectedRow();
                String pid=(String) productTable.getModel().getValueAt(row, 0);
                AddUpdateProductForm frame = new AddUpdateProductForm();
                frame.setData(pid);    
                this.setVisible(false);
                frame.setLocationRelativeTo(null);   
                frame.setDefaultCloseOperation(ManagementMain.DISPOSE_ON_CLOSE);
                frame.setVisible(true);
                
    }//GEN-LAST:event_updateButtonActionPerformed
